class Employee
{
private int Id;
private double Salary;
private String Name;


public void setId(int Id){
this.Id=Id;
}


public int getId(){
return Id;
}



public void setName(String Name){
this.Name= Name;
}

public String getName()
{
return Name;
}




public void setSalary(Double Salary){
this.Salary=Salary;
}

public double getSalary(){
return Salary;
}


}